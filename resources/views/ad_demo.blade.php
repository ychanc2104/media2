<div class="article-body" itemprop="articleBody">
<p>交通便利始終是購屋不敗主因，捷運綠線是桃園市首座自主興建的捷運綠線，正如火如荼施工中，沿線房市熱度不斷攀升，其中位在八德擴大重劃區的「八德站」受首購族、退休族喜愛，擁有廣豐新天地與大湳森林公園的「大湳站」周邊買氣同樣暢旺，加上1~2字頭親民房價，讓「八德站」、「大湳站」吸引三峽北大特區、竹科人口比價而來。</p>

<div class="ad text-wrap-around">
<div class="banner-336x280">
<div id="div-gpt-ad-1551198981140-4"></div>
</div>
</div>

<p></p>
<p></p>
<p></p>
<p>根據台慶不動產統計，2018年至2020年捷運綠線「六大站點周邊住宅交易價量變化」，分析周邊半徑800公尺內、屋齡2年以上的中古屋交易資料，扣除交易較少的橫山站、坑口站，發現「八德站」住宅均價增幅9.1%奪下榜首，且每年交易件數均維持在300件以上。</p>
<p></p>
<p></p>
<p></p>
<p>永慶不動產八德廣豐桃智加盟店店東陳文勝指出，「八德站」位於八德擴大重劃區，通勤至雙北市相當便捷，有多元公車班次可搭乘至土城捷運永寧站，未來不只有桃園捷運綠線，還有大鶯豐德交流道利多，將能大幅提升八德地區整體的交通環境，有望帶動房市買盤持續進場，因此不只有在地客購屋，還吸引土城、三峽、板橋以及台北市中心客層輕移民，尤以八德區公所生活圈受到首購族、退休族喜愛。</p>
<p></p>
<p></p>
<p></p>
<p>由於八德擴大重劃區是剛成形的重劃區，陳文勝分析，區內屋齡都在5~10年，景觀市容新穎，近年房價不斷攀升，這3年來幾乎以每年上揚1萬元的速度成長，加上去年房市火熱，「八德站」周邊房價一口氣漲了2~3萬元，因此均價突破2字頭，每坪房價來到21~25萬元行情，且各大建商仍在陸續推案中。</p>
<p></p>
<p></p>
<p></p>
<p>目前八德擴大重劃區已有20~30棟成型社區，主力總價為900~1100萬3房車、 700~900萬2房車，陳文勝舉例，如戶數多、設備完善的「墨爾本」，地段近交流道，正3房總價1000萬，很適合小家庭購屋，銷售流通速度快，指名度高；尋找低總價物件的民眾可選擇「美術館」，每坪單價20萬左右，是目前少數可能有機會19萬、出現1字頭的社區，2房車總價只要720萬元。</p>
<p></p>
<p></p>
<p></p>
<p>若想選擇4房車物件的換屋族，陳文勝推薦總價1300~1400萬的「雲觀」；至於預算較為足夠的民眾，則有「皇翔百老匯」、「宏普之星」等上市櫃建商推案可選擇，總價1500~1600萬元。</p>

<figure>
<div class="photo-container">
<img class="photo" loading="lazy" src="https://images.chinatimes.com/newsphoto/2021-06-11/1024/20210611002507.jpg" alt="捷運綠線八德站施工中。(圖/永慶房仲網提供)" width="1024" height="768">
</div>
<figcaption>捷運綠線八德站施工中。(圖/永慶房仲網提供)</figcaption>
</figure>

<p>另外，捷運綠線「大湳站」周邊買氣同樣暢旺！當地擁有廣豐新天地、大潤發、全聯、大湳森林公園等，區域生活與休閒機能較成熟，房價維持在親切的1、2字頭，深受在地自住買方青睞。陳文勝說，離「大湳站」車程不到5分鐘的義勇街，屋齡20~30年左右的3房型，平均每坪單價17~20萬元，總價大約只要600~700萬元，是不錯的選擇。</p>
<p></p>
<p></p>
<p></p>
<p>陳文勝表示，若預算較足，推薦廣豐重劃區的公園路、大智路生活圈，10分鐘內都可以到捷運「大湳站」，目前房價已來到3字頭，每坪25~30萬元買近10年大樓，3房車總價1000~1100萬元、4房車1400~1500萬元。如緊鄰廣豐新天地的「竹城六本木」，格局方正、生活機能完整，指名度相當高，或是豪宅案「桃大誠」，是在地品牌建商代表作品，每坪房價已突破3字頭。</p>
<p></p>
<p></p>
<p></p>
<p>【專家意見】</p>
